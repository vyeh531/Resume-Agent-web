function normalizeTitle(title) {
  return String(title || "")
    .toLowerCase()
    .replace(/\s+/g, " ")
    .trim();
}

function classifyPositionTitle(title) {
  const normalized = normalizeTitle(title);
  if (hasFullStackSignal(title)) return makeResult("software_engineer", "rule", ["software_engineer"]);
  const exact = EXACT_FAMILY.get(normalized);
  if (exact) return makeResult(exact, "exact", []);

  const matches = ORDERED_RULES
    .filter(([, re]) => re.test(normalized))
    .map(([family]) => family);
  const uniqueMatches = [...new Set(matches)];
  const family = uniqueMatches[0] || "other";
  return makeResult(family, uniqueMatches.length ? "rule" : "fallback", uniqueMatches);
}

function makeResult(canonicalRoleFamily, source, matches) {
  return {
    canonicalRoleFamily,
    roleGroup: ROLE_GROUP_BY_FAMILY[canonicalRoleFamily] || "other",
    source,
    matches,
  };
}

